// 2. 逻辑函数（依赖上面的数据）
function initItemSelector(itemCard, searchTerm = '') {
    const itemGrid = itemCard.querySelector('.item-grid');
    if (!itemGrid) return;

    // 直接使用内部数据
    let filteredItems = minecraftItems;
    if (searchTerm) {
        filteredItems = minecraftItems.filter(item => 
            item.id.toLowerCase().includes(searchTerm.toLowerCase()) || 
            item.name.toLowerCase().includes(searchTerm.toLowerCase())
        );
    }

    // 渲染逻辑（示例）
    itemGrid.innerHTML = filteredItems.map(item => `
        <div class="item">
            <img src="${item.image}" alt="${item.name}">
            <span>${item.name}</span>
        </div>
    `).join('');
}

// 3. 初始化（页面加载后执行）
document.addEventListener('DOMContentLoaded', () => {
    const itemCard = document.querySelector('.item-card');
    if (itemCard) {
        initItemSelector(itemCard); // 初始渲染
        // 绑定搜索事件等逻辑...
    }
});